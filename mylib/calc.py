"""
A calculator module
"""


def add(x, y):
    """
    Add two numbers
    """
    return x + y


# substract function
def substract(x, y):
    """
    Substract two numbers
    """
    return x - y


# multiplication function
def multiply(x, y):
    """
    Multiply two numbers
    """
    return x * y


# division function
def divide(x, y):
    """
    Divide two numbers
    """
    return x / y


# build a function to calculate the power of a number
def power(x, y):
    """
    Calculate the power of a number as x base y
    """
    return x**y


# build a function to calculate the square root
def square_root(x):
    """
    Return the square root of a number
    """
    return x**0.5
